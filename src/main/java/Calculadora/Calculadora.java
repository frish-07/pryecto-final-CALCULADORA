    private void botonIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIgualActionPerformed
       double pi=3.141592;
        Double segundonumero;      
        if (operacion.equals("nula")) {
            etiquetanumero.setText(cadenanum);
        }
        else if (operacion.equals("sumar")) {
            segundonumero = Double.parseDouble(cadenanum);
            if ((primernumero == pi) || (segundonumero == pi)) {
                resultado = primernumero + segundonumero;
                etiquetanumero.setText(String.format("% .6f", resultado));
                cadenanum = String.valueOf(resultado);
                operacion = "nula";
            } else {
                resultado = primernumero + segundonumero;
                etiquetanumero.setText(String.format("% .2f", resultado));
                cadenanum = String.valueOf(resultado);
                operacion = "nula";
            }
        }
        
        else if (operacion.equals("restar")) {
            segundonumero = Double.parseDouble(cadenanum);
            if ((primernumero == pi) || (segundonumero == pi)) {
                resultado = primernumero - segundonumero;
                etiquetanumero.setText(String.format("%.6f", resultado));
                cadenanum = String.valueOf(resultado);
                operacion = "nula";
            } else {
                resultado = primernumero - segundonumero;
                etiquetanumero.setText(String.format("%.2f", resultado));
                cadenanum = String.valueOf(resultado);
                operacion = "nula";
            }
        }
        
        else if (operacion.equals("multiplicar")) {
            segundonumero = Double.parseDouble(cadenanum);
            if ((primernumero == pi) || (segundonumero == pi)) {
                resultado = primernumero * segundonumero;
                etiquetanumero.setText(String.format("%.6f", resultado));
                cadenanum = String.valueOf(resultado);
                operacion = "nula";
            } else {
                resultado = primernumero * segundonumero;
                etiquetanumero.setText(String.format("%.2f", resultado));
                cadenanum = String.valueOf(resultado);
                operacion = "nula";
            }
        }
           
        else if (operacion.equals("porcentaje")) {
            segundonumero = Double.parseDouble(cadenanum);
            resultado = primernumero * 0.01 * segundonumero;
            etiquetanumero.setText(String.format("%.2f", resultado));
            cadenanum = String.valueOf(resultado);
            operacion = "nula";

        }
        else if (operacion.equals("pow")) {
            segundonumero=Double.parseDouble(cadenanum);
            resultado=Math.pow(primernumero,segundonumero);
            etiquetanumero.setText(String.format("%.2f", resultado));
            cadenanum=String.valueOf(resultado);
            operacion="nula" ;         
        }  
                     
         else if (operacion.equals("dividir")) {
            segundonumero = Double.parseDouble(cadenanum);
            if ((primernumero == pi) || (segundonumero == pi)) {
                if (segundonumero == 0) {
                    etiquetanumero.setText("indeterminado");
                } else {
                    resultado = primernumero / segundonumero;
                    etiquetanumero.setText(String.format("%.6f", resultado));
                    cadenanum = String.valueOf(resultado);
                    operacion = "nula";
                }
            } else {
                if (segundonumero == 0) {
                    etiquetanumero.setText("indeterminado");
                } else {
                    resultado = primernumero / segundonumero;
                    etiquetanumero.setText(String.format("%.2f", resultado));
                    cadenanum = String.valueOf(resultado);
                    operacion = "nula";
                }
            }
        }     
        etiquetamuestra.setText("");
        activado =true;
        punto=false;
    }//GEN-LAST:event_botonIgualActionPerformed

